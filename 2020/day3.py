def trees(right, down):
  horiz_pos = 0
  pattern_length = len(input.split('\n')[0])
  count = 0
  for line in [l for i, l in enumerate(input.split('\n')) if i > 0 and i%down == 0]:
    horiz_pos = horiz_pos + right
    if horiz_pos >= pattern_length:
      horiz_pos = horiz_pos - pattern_length
    if (line[horiz_pos] == '#'):
      count = count + 1
  return count

